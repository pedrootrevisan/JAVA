public class ThreeElement2  {

    private int a;
    private int b;
    private int c;

    public ThreeElement2(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int sum() {
        return a + b + c;
    }

    public int product() {
        return a * b * c;
    }
}